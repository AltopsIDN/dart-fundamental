void main(List<String> args) {
  // ! artinya bukan / not jadi dia kayak kebalikan ( != = ! + =)
  // ! ==> kebalikan
  // = ==> adalah
  // == ==> membandingkan sama
  // === ==> membandingkan sama identik (sama persis)
  // != ==> membandingkan tidak sama dengan
  // !== ==> membandingkan tidak sama identik
  var isTrue = true;
  var isFalse = false;

  print(isTrue); // ==> true
  print(!isFalse); // ==> true
  print(isTrue != false); // ==> true
  // true (tidak sama dengan) false adalah true

  print(isFalse = false); // ==> false

}